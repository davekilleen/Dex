#!/usr/bin/env node
/**
 * sync-skill-commands.cjs
 *
 * Makes Dex skills show up in the Claude Code slash-command dropdown.
 *
 * Claude Code's "/" autocomplete only reads `.claude/commands/*.md`. Dex skills
 * live in `.claude/skills/<name>/SKILL.md`, so they're invokable but invisible.
 * This script scans every skill, reads its frontmatter (name + description), and
 * writes a thin command stub into `.claude/commands/` that delegates back to the
 * skill. The `description` carries into the dropdown so you can browse what Dex
 * can do as you type.
 *
 * Run it once now, and again any time you add or rename a skill:
 *   node .scripts/sync-skill-commands.cjs
 *
 * Stubs are regenerated every run (idempotent). Stale stubs whose skill no longer
 * exists are removed. Stubs carry a marker comment so hand-written commands are
 * never touched.
 *
 * It also (a) writes a categorized `.claude/skills/CATALOG.md` that the /dex
 * command renders, and (b) installs a git pre-commit hook so the dropdown
 * re-syncs automatically whenever skills change. Because `.git/hooks` isn't
 * version-controlled, running this script once after a fresh clone restores the
 * hook — which is why `/setup` runs it on install.
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SKILLS_DIR = path.join(ROOT, '.claude', 'skills');
const COMMANDS_DIR = path.join(ROOT, '.claude', 'commands');
const CATALOG_FILE = path.join(SKILLS_DIR, 'CATALOG.md');
const HOOK_FILE = path.join(ROOT, '.git', 'hooks', 'pre-commit');
const MARKER = '<!-- auto-generated by sync-skill-commands.cjs — do not edit by hand -->';
const HOOK_MARKER = '# dex-skill-command-sync';

const HOOK_BODY = `#!/bin/sh
${HOOK_MARKER}
# Dex: keep the slash-command dropdown in sync with skills.
# When any .claude/skills/ file is staged, regenerate command stubs and stage them.
# Installed/maintained by .scripts/sync-skill-commands.cjs — safe to re-run.

if git diff --cached --name-only | grep -q '^\\.claude/skills/'; then
  echo "[dex] Skills changed — syncing slash-command stubs…"
  if command -v node >/dev/null 2>&1; then
    node .scripts/sync-skill-commands.cjs >/dev/null
    git add .claude/commands/ .claude/skills/CATALOG.md
    echo "[dex] Command dropdown synced."
  else
    echo "[dex] node not found; skipping skill-command sync." >&2
  fi
fi
`;

// Category map for the /dex catalog. Skills not listed fall into "Other".
// Order here is the order categories appear in the catalog.
const CATEGORIES = [
  ['📅 Daily & Weekly', ['daily-plan', 'daily-review', 'week-plan', 'week-review', 'journal']],
  ['🎯 Quarterly & Strategy', ['quarter-plan', 'quarter-review', 'industry-truths', 'product-brief', 'identity-snapshot']],
  ['🤝 Meetings', ['meeting-prep', 'log-meeting', 'process-meetings', 'commitment-scan']],
  ['💰 Sales & Pipeline', ['pipeline-sync']],
  ['📁 Projects & Tasks', ['triage', 'project-health', 'save-insight']],
  ['🚀 Career', ['career-setup', 'career-coach', 'resume-builder']],
  ['🔌 Setup & Integrations', ['setup', 'getting-started', 'calendar-setup', 'granola-setup', 'google-workspace-setup', 'ms-teams-setup', 'zoom-setup', 'atlassian-setup', 'todoist-setup', 'trello-setup', 'things-setup', 'screenpipe-setup', 'enable-semantic-search', 'ai-setup', 'ai-status', 'create-mcp', 'dex-add-mcp', 'integrate-mcp', 'dex-obsidian-setup', 'beta-activate', 'beta-status']],
  ['🛠️ System & Maintenance', ['dex-update', 'dex-rollback', 'dex-whats-new', 'dex-level-up', 'dex-backlog', 'dex-improve', 'dex-demo', 'health-check', 'reset', 'create-skill', 'prompt-improver', 'xray', 'scrape']],
];

// Skills that are general-purpose tooling rather than Dex workflow commands.
// Excluded by default to keep the dropdown focused. Remove a name to surface it.
const EXCLUDE = new Set([
  'anthropic-algorithmic-art',
  'anthropic-brand-guidelines',
  'anthropic-canvas-design',
  'anthropic-doc-coauthoring',
  'anthropic-docx',
  'anthropic-frontend-design',
  'anthropic-internal-comms',
  'anthropic-mcp-builder',
  'anthropic-pdf',
  'anthropic-pptx',
  'anthropic-skill-creator',
  'anthropic-slack-gif-creator',
  'anthropic-theme-factory',
  'anthropic-web-artifacts-builder',
  'anthropic-webapp-testing',
  'anthropic-xlsx',
]);

function parseFrontmatter(text) {
  // Normalize CRLF→LF (Windows files keep a trailing \r that breaks the $ anchor),
  // strip UTF-8 BOM, and drop leading whitespace before the frontmatter fence.
  text = text.replace(/\r\n/g, '\n').replace(/^﻿/, '').replace(/^\s+/, '');
  if (!text.startsWith('---')) return null;
  const end = text.indexOf('\n---', 3);
  if (end === -1) return null;
  const block = text.slice(3, end);
  const out = {};
  for (const line of block.split('\n')) {
    const m = line.match(/^([A-Za-z0-9_]+):\s*(.*)$/);
    if (m && out[m[1]] === undefined) {
      let val = m[2].trim();
      if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
        val = val.slice(1, -1);
      }
      out[m[1]] = val;
    }
  }
  return out;
}

function discoverSkills() {
  const skills = [];
  for (const entry of fs.readdirSync(SKILLS_DIR, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    if (entry.name.startsWith('_')) continue;
    if (EXCLUDE.has(entry.name)) continue;
    const skillFile = path.join(SKILLS_DIR, entry.name, 'SKILL.md');
    if (!fs.existsSync(skillFile)) continue;
    const fm = parseFrontmatter(fs.readFileSync(skillFile, 'utf8')) || {};
    skills.push({
      name: fm.name || entry.name,
      dir: entry.name,
      description: (fm.description || '').replace(/\s+/g, ' ').trim(),
    });
  }
  return skills;
}

function buildStub(skill) {
  const desc = skill.description || `Run the ${skill.name} skill.`;
  return `---
description: ${desc}
---
${MARKER}

Invoke the **${skill.name}** Dex skill.

Read \`.claude/skills/${skill.dir}/SKILL.md\` and follow its instructions exactly.

Arguments passed by the user: $ARGUMENTS
`;
}

function isGeneratedStub(file) {
  try {
    return fs.readFileSync(file, 'utf8').includes(MARKER);
  } catch {
    return false;
  }
}

function buildCatalog(skills) {
  const byName = new Map(skills.map((s) => [s.name, s]));
  const seen = new Set();
  const lines = [
    MARKER,
    '',
    '# Dex Skill Catalog',
    '',
    'Every skill you can run, grouped by what it does. Type `/` in the chat box to',
    'fuzzy-search any of these, or just ask Dex in plain language.',
    '',
  ];

  for (const [label, names] of CATEGORIES) {
    const present = names.filter((n) => byName.has(n));
    if (!present.length) continue;
    lines.push(`## ${label}`, '');
    for (const n of present) {
      seen.add(n);
      const s = byName.get(n);
      lines.push(`- **/${n}** — ${s.description || 'No description'}`);
    }
    lines.push('');
  }

  const leftovers = skills.filter((s) => !seen.has(s.name));
  if (leftovers.length) {
    lines.push('## 📦 Other', '');
    for (const s of leftovers) {
      lines.push(`- **/${s.name}** — ${s.description || 'No description'}`);
    }
    lines.push('');
  }

  lines.push(`_${skills.length} skills total. Regenerated by \`node .scripts/sync-skill-commands.cjs\`._`);
  return lines.join('\n') + '\n';
}

function installHook() {
  // .git/hooks isn't version-controlled, so a fresh clone won't have it.
  // Install (or refresh) the pre-commit hook so auto-sync survives re-installs.
  const hooksDir = path.dirname(HOOK_FILE);
  if (!fs.existsSync(hooksDir)) {
    return false; // not a git repo (or hooks dir missing) — skip silently
  }
  if (fs.existsSync(HOOK_FILE)) {
    const existing = fs.readFileSync(HOOK_FILE, 'utf8');
    if (!existing.includes(HOOK_MARKER)) {
      // A hand-written pre-commit hook exists — don't clobber it.
      console.log('⚠️  Existing pre-commit hook found (not ours) — left untouched.');
      console.log('   Add this line to it to keep the dropdown synced:');
      console.log('   node .scripts/sync-skill-commands.cjs && git add .claude/commands/ .claude/skills/CATALOG.md');
      return false;
    }
  }
  fs.writeFileSync(HOOK_FILE, HOOK_BODY, 'utf8');
  try {
    fs.chmodSync(HOOK_FILE, 0o755);
  } catch {
    /* chmod is a no-op on some Windows filesystems; git for Windows still runs it */
  }
  return true;
}

function main() {
  if (!fs.existsSync(COMMANDS_DIR)) fs.mkdirSync(COMMANDS_DIR, { recursive: true });

  const hookInstalled = installHook();

  const skills = discoverSkills();
  const wanted = new Set(skills.map((s) => `${s.name}.md`));

  let created = 0;
  for (const skill of skills) {
    const target = path.join(COMMANDS_DIR, `${skill.name}.md`);
    // Never clobber a hand-written command that lacks our marker.
    if (fs.existsSync(target) && !isGeneratedStub(target)) {
      console.log(`skip (hand-written): ${skill.name}.md`);
      continue;
    }
    fs.writeFileSync(target, buildStub(skill), 'utf8');
    created++;
  }

  // Remove stale generated stubs whose skill no longer exists.
  let removed = 0;
  for (const file of fs.readdirSync(COMMANDS_DIR)) {
    if (!file.endsWith('.md')) continue;
    if (wanted.has(file)) continue;
    const full = path.join(COMMANDS_DIR, file);
    if (isGeneratedStub(full)) {
      fs.unlinkSync(full);
      removed++;
      console.log(`removed stale: ${file}`);
    }
  }

  // Regenerate the categorized catalog that /dex renders.
  fs.writeFileSync(CATALOG_FILE, buildCatalog(skills), 'utf8');

  console.log(`\n✅ Synced ${created} skill command(s) → .claude/commands/`);
  if (removed) console.log(`🧹 Removed ${removed} stale stub(s)`);
  console.log('📖 Catalog written → .claude/skills/CATALOG.md');
  if (hookInstalled) console.log('🪝 Pre-commit hook installed → auto-syncs on future commits');
  console.log('Restart Claude Code (or reopen the session) to refresh the dropdown.');
}

main();
